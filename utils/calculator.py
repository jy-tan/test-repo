import math


def add(a, b):
    return a + b

def divide(a, b):
    return a / b

def multiply(a, b):
    return a * b

def subtract(a, b):
    return a - b

def sqrt(a):
    return a ** 0.5

def log(a):
    return math.log(a)

def exp(a):
    return math.exp(a)

def sin(a):
    return math.sin(a)

def cos(a):
    return math.cos(a)

