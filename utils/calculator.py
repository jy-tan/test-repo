import math


def add(a, b):
    return a + b

def divide(a, b):
    return a / b

def multiply(a, b):
    return a * b

def subtract(a, b):
    return a - b

def sqrt(a):
    return a ** 0.5

def cube_root(a):
    return a ** (1/3)

def log(a):
    return math.log(a)

def tan(a):
    return math.tan(a)

def tanh(a):
    return math.tanh(a)